public class Objective6Lab3 {
  public static void main(String[] args){

    int counter = 1;

    /*write a while loop */
    while (counter <= 20){
      
      /*counter variable execute 20 times */
      if (counter % 2 == 0 ){
        /*write a if statement determines if number is odd or even % */
        /*display counter is even or is odd */
        System.out.println(counter + " is even ");
      } else {
        System.out.println(counter +" is odd ");
      }

    counter = counter +1;
    }
  } /*end of while loop*/
} /*end block*/
